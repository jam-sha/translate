def filterPair(p, length):
    return len(p[0].split(' ')) < length and \
        len(p[1].split(' ')) < length and \
        p[1].startswith(eng_prefixes)

def filterPairs(pairs, length):
    return [pair for pair in pairs if filterPair(pair, length)]

